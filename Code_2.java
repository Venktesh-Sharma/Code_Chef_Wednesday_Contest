import java.util.*;
public class Code_2 {
    public static void main(String[] args) throws java.lang.Exception {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        if (n >= 5) {
            System.out.println(n * 85);
        } else {
            System.out.println(n * 100);
        }
    }
}

